namespace Wavee;

/// <summary>
/// An enum representing the relative size of the artwork.
/// </summary>
public enum ArtworkSizeType
{
    /// <summary>
    /// The default size of the artwork. 
    /// </summary>
    Default,
    /// <summary>
    /// Small size of the artwork relative to the default size.
    /// </summary>
    Small,
    /// <summary>
    /// Large size of the artwork relative to the default size.
    /// </summary>
    Large,
    /// <summary>
    /// Extra large size of the artwork relative to the default size.
    /// </summary>
    ExtraLarge,
}